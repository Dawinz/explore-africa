/**
 * Get major cities for a country
 */
export const getMajorCities = (countryName) => {
  if (!countryName) return [];
  
  const name = countryName.toLowerCase().trim();
  return countryMajorCities[name] || [];
};

/**
 * Get cities with capital first, then other major cities
 */
export const getCitiesForCountry = (country) => {
  if (!country) return [];
  
  const capitals = Array.isArray(country.capital) ? country.capital : [];
  const countryName = country.name?.toLowerCase().trim() || '';
  const majorCities = getMajorCities(countryName);
  
  // If we have major cities defined, use them (they already include capitals)
  if (majorCities.length > 0) {
    // Ensure capitals are first if they exist
    const cities = [];
    
    // Add capitals first
    for (const capital of capitals) {
      if (capital && !cities.includes(capital)) {
        cities.push(capital);
      }
    }
    
    // Add other major cities that aren't already in the list
    for (const city of majorCities) {
      if (!cities.includes(city) && cities.length < 5) {
        cities.push(city);
      }
    }
    
    // If we still don't have enough, use major cities list directly
    if (cities.length < 3 && majorCities.length > 0) {
      return majorCities.slice(0, 5);
    }
    
    return cities.slice(0, 5);
  }
  
  // Fallback: just use capitals if available
  if (capitals.length > 0) {
    return capitals.slice(0, 5);
  }
  
  return [];
};

export default {
  countryMajorCities,
  getMajorCities,
  getCitiesForCountry,
};

